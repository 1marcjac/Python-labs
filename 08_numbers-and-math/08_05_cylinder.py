# Write the necessary code calculate the volume and surface area
# of a cylinder with a radius of 3.14 and a height of 5.
# Print out the result.
radius = 3.14 
height = 5
volume = 2*(3.14**2)*height 
surface_area = 2*3.14*radius*(height+radius)
print (f"The surface area of a cylindar is {surface_area}, the volume of a cylindar is {volume}")
